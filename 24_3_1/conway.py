    newCells = copy.deepcopy(cells)
    for row, cellsRow in enumerate(cells):
        for column, cell in enumerate(cellsRow):
            newCells[row][column] = aliveNext(cells=cells, row=row, column=column)
    cells = copy.deepcopy(newCells)
    printCells(cells=cells)